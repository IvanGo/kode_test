import UIKit

extension UIColor {
    class var kdMainBackground: UIColor {
        return UIColor(colorLiteralRed: 30 / 255.0, green: 88 / 255.0, blue: 207 / 255.0, alpha: 1)
    }
    
    class var kdActiveElement: UIColor {
        return UIColor.white
    }
    
    class var kdDescription: UIColor {
        return UIColor.gray
    }
}
